"""
    Data Types in Python3.7
"""
# Integer Type
# Base 10
print('Decimal Base')
print(type(12345689495))
print(12345689495)

# Binary Base
print('Binary Base')
print(type(0b1001))
print('0b1001 = {}'.format(0b1001))
# Decimal to Binary base
# bin()
print('8 --> {}'.format(bin(8)))

# Octal Base
print('Octal Base')
print(type(0o3))
print('0o77 = {}'.format(0o77))
# Decimal to Octal Base
# oct()
print('16 --> {}'.format(oct(16)))

# Hex Base
print('Hex Base')
print(type(0xFF))
print('0xFF = {}'.format(0xFF))
print('0xFF + 0x01 = {}'.format(0xFF+0x01))

# Decimal to Hex base
# hex()
print('255 --> {}'.format(hex(255)))
